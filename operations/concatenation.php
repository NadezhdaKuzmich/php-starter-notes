<?php
/*
Арифметичні та строкові операції
Для роботи з рядками є всього два оператори:
• Конкатенація ('.')
• Привласнення з конкатенацією ('.=')
*/

// Конкатенація
$name = 'John';
$secondName = 'Doe';

echo '1. His name is ' . $name . ' ' . $secondName;
echo '<br>';

// Привласнення з конкатенацією 
$hello = '2. Hello ';
$hello .= 'world!';

echo $hello;